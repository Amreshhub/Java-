//EXAMPLE TO DEMONSTRATE INTEGER LITERAL CONSTENT
class intliteral
{
	public static void main(String s[])
	{
		int a=79,b=0117,c=0x4f;
	System.out.println("Value of var a:-"+a);
	System.out.println("Value of var b:-"+b);
	System.out.println("Value of var c:-"+c);
	System.out.printf("\nVar a in octal num:-%#o",a);
	System.out.printf("\nVar b in Hexadecimal num:-%#x",b);
	System.out.printf("\nVar c in octal num:-%d",c);
		}//close of main
	}//close of class